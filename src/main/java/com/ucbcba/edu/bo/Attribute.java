package com.ucbcba.edu.bo;

public class Attribute {
    public String type;
    public String name;
    public String key;
    private String keyTec;



    public Attribute(String type, String name, String Key, String KeyTec) {
        this.type = type;
        this.name = name;
        key = Key;
        keyTec = KeyTec;
    }
}
